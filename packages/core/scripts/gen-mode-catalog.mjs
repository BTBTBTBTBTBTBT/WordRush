#!/usr/bin/env node
// Codegen: packages/core/modes.json → native mode-catalog files.
// Run: `pnpm gen:modes` (from repo root). Web imports modes.json directly; this
// script emits the Swift + Kotlin equivalents so name/accent/glyph/order/dbKey
// live in ONE place. Icons stay platform-native (keyed by id/dbKey).
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const root = join(here, '..', '..', '..');
const modes = JSON.parse(readFileSync(join(here, '..', 'modes.json'), 'utf8')).modes;

const swiftOpt = (v) => (v == null ? 'nil' : `"${v}"`);
const ktOpt = (v) => (v == null ? 'null' : `"${v}"`);
const hexToUInt = (h) => '0x' + h.replace('#', '').toUpperCase();

// ── Swift ────────────────────────────────────────────────────────────────────
const swift = `// GENERATED by packages/core/scripts/gen-mode-catalog.mjs from modes.json — DO NOT EDIT.
// Run \`pnpm gen:modes\` to regenerate. Edit packages/core/modes.json instead.
import SwiftUI

struct GenMode: Identifiable {
    let id: String
    let dbKey: String?
    let title: String
    let shortTitle: String
    let shareLabel: String
    let desc: String
    let accentHex: String
    let glyph: String?
    let romanNumeral: String?
    let dailyEligible: Bool
    var accent: Color { Color(hexString: accentHex) ?? .purple }
}

enum ModeGen {
    static let all: [GenMode] = [
${modes.map((m) => `        GenMode(id: "${m.id}", dbKey: ${swiftOpt(m.dbKey)}, title: "${m.title}", shortTitle: "${m.shortTitle}", shareLabel: "${m.shareLabel}", desc: "${m.desc}", accentHex: "${m.accentHex}", glyph: ${swiftOpt(m.glyph)}, romanNumeral: ${swiftOpt(m.romanNumeral)}, dailyEligible: ${m.dailyEligible})`).join(',\n')},
    ]
    static func byDbKey(_ k: String) -> GenMode? { all.first { $0.dbKey == k } }
    static func byId(_ i: String) -> GenMode? { all.first { $0.id == i } }
    /// Daily modes (VS excluded), in canonical order — used by the all-dailies card.
    static var daily: [GenMode] { all.filter { $0.dailyEligible && $0.dbKey != nil } }
}
`;

// ── Kotlin ───────────────────────────────────────────────────────────────────
const kotlin = `// GENERATED by packages/core/scripts/gen-mode-catalog.mjs from modes.json — DO NOT EDIT.
// Run \`pnpm gen:modes\` to regenerate. Edit packages/core/modes.json instead.
package com.wordocious.app

import androidx.compose.ui.graphics.Color

data class GenMode(
    val id: String,
    val dbKey: String?,
    val title: String,
    val shortTitle: String,
    val shareLabel: String,
    val desc: String,
    val accentHex: String,
    val glyph: String?,
    val romanNumeral: String?,
    val dailyEligible: Boolean,
) {
    val accent: Color get() = Color(("ff" + accentHex.removePrefix("#")).toLong(16))
    val accentInt: Int get() = ("ff" + accentHex.removePrefix("#")).toLong(16).toInt()
}

object ModeGen {
    val all: List<GenMode> = listOf(
${modes.map((m) => `        GenMode("${m.id}", ${ktOpt(m.dbKey)}, "${m.title}", "${m.shortTitle}", "${m.shareLabel}", "${m.desc}", "${m.accentHex}", ${ktOpt(m.glyph)}, ${ktOpt(m.romanNumeral)}, ${m.dailyEligible})`).join(',\n')},
    )
    fun byDbKey(k: String): GenMode? = all.firstOrNull { it.dbKey == k }
    fun byId(i: String): GenMode? = all.firstOrNull { it.id == i }
    val daily: List<GenMode> get() = all.filter { it.dailyEligible && it.dbKey != null }
}
`;

// ── Web (TS) ─────────────────────────────────────────────────────────────────
const ts = `// GENERATED by packages/core/scripts/gen-mode-catalog.mjs from modes.json — DO NOT EDIT.
// Run \`pnpm gen:modes\` to regenerate. Edit packages/core/modes.json instead.

export interface ModeMeta {
  id: string;
  dbKey: string | null;
  title: string;
  shortTitle: string;
  shareLabel: string;
  desc: string;
  accentHex: string;
  glyph: string | null;
  romanNumeral: string | null;
  dailyEligible: boolean;
}

export const MODES: ModeMeta[] = ${JSON.stringify(modes, null, 2)};

export const MODE_BY_ID: Record<string, ModeMeta> = Object.fromEntries(MODES.map((m) => [m.id, m]));
export const MODE_BY_DBKEY: Record<string, ModeMeta> = Object.fromEntries(
  MODES.filter((m) => m.dbKey).map((m) => [m.dbKey as string, m]),
);
/** Daily modes (VS excluded), canonical order. */
export const DAILY_MODES: ModeMeta[] = MODES.filter((m) => m.dailyEligible && m.dbKey);
`;

const swiftPath = join(root, 'apps/ios/Wordocious/Sources/ModeCatalog.generated.swift');
const ktPath = join(root, 'apps/android/app/src/main/kotlin/com/wordocious/app/ModeCatalog.generated.kt');
const tsPath = join(root, 'apps/web/lib/modes.generated.ts');
writeFileSync(swiftPath, swift);
writeFileSync(ktPath, kotlin);
writeFileSync(tsPath, ts);
console.log(`gen:modes — wrote ${modes.length} modes →`);
console.log(`  ${swiftPath}`);
console.log(`  ${ktPath}`);
console.log(`  ${tsPath}`);
